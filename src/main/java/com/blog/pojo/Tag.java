package com.blog.pojo;


/**
 * Description: 标签表
 * 
 * @author Todcsw
 * @date  
 */
public class Tag {


  private String name;
  private String uname;
  private int id;

  public String getName() {
    return name;
  }

  public void setName(String name) {
    this.name = name;
  }

  public String getUname() {
    return uname;
  }

  public void setUname(String uname) {
    this.uname = uname;
  }

  public int getId() {
    return id;
  }

  public void setId(int id) {
    this.id = id;
  }
}
